package com.quelotic.mealcounter;

public class Meal {
    private int id;
    private String date;
    private int plates;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getPlates() {
        return plates;
    }

    public void setPlates(int plates) {
        this.plates = plates;
    }

}
